                <h3>Jim Huang (jserv)</h3>
                <p class="intro">
                  Jim Huang (黃敬群)，網路慣用暱稱是 "jserv"，國立成功大學資訊工程系兼任教師，曾參與若干手機、數位電視、導航裝置，以及
                  CPU 的設計與研發。自 1999 年起，投入開放原始碼軟體、自由軟體的開發工作，包含新酷音輸入法, GCC / GNU
                  Classpath, LXDE, Android Open Source Project
                  等世界級的專案，並多次在國內外資訊技術的研討會發表研發成果。2012
                  年搬到台南生活後，除了開設一系列系統層級軟體設計與實作的課程外，也邀約開放資訊技術的同好，一同在充滿陽光與古色古香的府城，舉辦 MOSUT
                  (Meet Open Source Users in Tainan) 的定期聚會。
